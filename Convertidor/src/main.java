    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        double dolar = 0.050;
        double chile = 48.83;
        double euro = 0.051; 
        double yen = 7.48;
        double argentino = 7.62483;
        double Cop = 0.0040;
        double Dolar_canadiense = 14.50;
        double won = 72.12;
        double Yuan = 2.71;
        double real_brasile침o = 3.83;
        

        
        
       String datoPesosIngresados = pesos.getText();
       int datoPesoPaisIngresadosInt = Integer.parseInt(datoPesosIngresados);
        
        
        String moneda= "";
        
        if ("Dolar".equals(monedasPaises.getSelectedItem())){
              moneda = "1";
           }
                if ("Euro".equals(monedasPaises.getSelectedItem())){
              moneda = "2";
           }
                        if ("Chile".equals(monedasPaises.getSelectedItem())){
               moneda = "3";
           }
                                if ("Argentino".equals(monedasPaises.getSelectedItem())){
              moneda = "4";
           }
                                        if ("Yen".equals(monedasPaises.getSelectedItem())){
              moneda = "5";
           }
                                            if ("Cop".equals(monedasPaises.getSelectedItem())){
              moneda = "6";
           }
                                                if ("Won".equals(monedasPaises.getSelectedItem())){
              moneda = "8";
           }
                                                    if ("Yuan".equals(monedasPaises.getSelectedItem())){
              moneda = "9";
           }
                                                        if ("real brasile침o".equals(monedasPaises.getSelectedItem())){
              moneda = "10";
           }
                                                
       int monedaInt = Integer.parseInt(moneda);
        
      
       if(monedaInt == 1){
        resultConvDigito = datoPesoPaisIngresadosInt * dolar;
         resultConversion = Math.round(resultConvDigito) + " dolares.";
       }
       if(monedaInt == 2){
         
         resultConvDigito = datoPesoPaisIngresadosInt * chile;
         resultConversion = Math.round(resultConvDigito) + " peso Chileno.";
       }
       if(monedaInt == 3){
         resultConvDigito = datoPesoPaisIngresadosInt * euro;
           resultConversion = Math.round(resultConvDigito) + " euros.";
       }
       if(monedaInt == 4){
         resultConvDigito = datoPesoPaisIngresadosInt * yen;
         resultConversion = Math.round(resultConvDigito) + " Yen.";
       }
       if(monedaInt == 5){
         resultConvDigito = datoPesoPaisIngresadosInt * argentino;
         resultConversion = Math.round(resultConvDigito) + " Peso argentino.";
       }
         if(monedaInt == 6){
         resultConvDigito = datoPesoPaisIngresadosInt * Cop;
         resultConversion = Math.round(resultConvDigito) + " Peso colombiano.";
       }
       if(monedaInt == 7){
       resultConvDigito = datoPesoPaisIngresadosInt * Dolar_canadiense;
         resultConversion = Math.round(resultConvDigito) + " Dolar canadiense.";
       }
       if(monedaInt == 8){
        resultConvDigito = datoPesoPaisIngresadosInt * won;
         resultConversion = Math.round(resultConvDigito) + " Won.";
       }
       if(monedaInt == 9){
        resultConvDigito = datoPesoPaisIngresadosInt * Yuan;
         resultConversion = Math.round(resultConvDigito) + " yuan.";
       }
      if(monedaInt == 10){
         resultConvDigito = datoPesoPaisIngresadosInt * real_brasile침o;
         resultConversion = Math.round(resultConvDigito) + " real brasile침o";
       }

       agregar();
        
        
    }//GEN-LAST:event_btnADDActionPerformed

